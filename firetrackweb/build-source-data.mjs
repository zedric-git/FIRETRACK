import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const webRoot = path.dirname(new URL(import.meta.url).pathname).replace(/^\/([A-Za-z]:)/, "$1");
const projectRoot = path.resolve(webRoot, "..", "CPE262_FINAL_PROJECT");
const outputPath = path.join(webRoot, "source-data.js");
const allowedExtensions = new Set([".cs", ".xaml", ".html", ".sql"]);
const skippedDirectories = new Set(["bin", "obj", ".vs", ".git"]);
const skippedPresentationFiles = new Set(["views/firetrackdialog.cs"]);

function isGeneratedFile(fileName, content) {
  const lower = fileName.toLowerCase();
  return lower.endsWith(".g.cs")
    || lower.endsWith(".g.i.cs")
    || lower.endsWith(".designer.cs")
    || lower.endsWith(".assemblyinfo.cs")
    || lower.includes("temporarygeneratedfile_")
    || /<auto-generated|auto-generated by/i.test(content.slice(0, 1200));
}

function isPresentationInternalFile(relativePath) {
  const normalized = relativePath.toLowerCase();
  return skippedPresentationFiles.has(normalized)
    || normalized.includes("cleanup_unused_schema")
    || normalized.includes("migration_barangay_chosen");
}

function languageFor(extension) {
  if (extension === ".cs") return "csharp";
  if (extension === ".sql") return "sql";
  if (extension === ".xaml") return "xaml";
  return "html";
}

async function collectFiles(directory, relativeBase = "") {
  const entries = await readdir(directory, { withFileTypes: true });
  const sorted = entries.sort((a, b) => {
    if (a.isDirectory() !== b.isDirectory()) return a.isDirectory() ? -1 : 1;
    return a.name.localeCompare(b.name);
  });

  const files = [];
  for (const entry of sorted) {
    if (entry.isDirectory()) {
      if (skippedDirectories.has(entry.name)) continue;
      files.push(...await collectFiles(path.join(directory, entry.name), path.join(relativeBase, entry.name)));
      continue;
    }

    const extension = path.extname(entry.name).toLowerCase();
    if (!allowedExtensions.has(extension)) continue;

    const absolutePath = path.join(directory, entry.name);
    const content = await readFile(absolutePath, "utf8");
    const relativePath = path.join(relativeBase, entry.name).replace(/\\/g, "/");
    if (isGeneratedFile(entry.name, content) || isPresentationInternalFile(relativePath)) continue;

    files.push({
      path: relativePath,
      name: entry.name,
      extension,
      language: languageFor(extension),
      content,
      lineCount: content.length === 0 ? 1 : content.split(/\r\n|\r|\n/).length,
    });
  }

  return files;
}

const files = await collectFiles(projectRoot);
const defaultPath = files.find((file) => file.path === "MainWindow.xaml.cs")?.path ?? files[0]?.path ?? "";
const payload = {
  projectName: "CPE262_FINAL_PROJECT",
  generatedAt: new Date().toISOString(),
  defaultPath,
  files,
};

await writeFile(
  outputPath,
  `window.FIRETRACK_SOURCE_DATA = ${JSON.stringify(payload)};\n`,
  "utf8",
);

console.log(`Embedded ${files.length} source files in ${outputPath}`);
